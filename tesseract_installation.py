import webbrowser
"""This code opens the specified url in the user's preferred web browser"""


def open_for_windows():
    url_win = 'https://github.com/UB-Mannheim/tesseract/wiki'
    webbrowser.open(url_win)


def open_for_linux():
    url_mac_linux = 'https://tesseract-ocr.github.io/tessdoc/Installation.html'
    webbrowser.open(url_mac_linux)


def open_for_mac():
    url_mac_linux = 'https://tesseract-ocr.github.io/tessdoc/Installation.html'
    webbrowser.open(url_mac_linux)
